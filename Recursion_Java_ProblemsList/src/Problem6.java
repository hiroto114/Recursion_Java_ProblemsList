
/**
 * Julian は旅行で日本を訪れています。
 * 日本では長さの単位はメートル法で定められていますが、Julian はヤードポンド法に慣れているので、
 * メートルを瞬時にマイルに変換しようと考えました。
 * メートル meters が与えられるので、それをマイルに変換する、metersToMiles という関数を定義してください。
 * なお、1 メートル = 0.000621371 マイルとして計算してください。
 */
public class Problem6 {
    public static double metersToMiles(int meters){
        return 0.000621371*meters;
    }
}
